    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $db = new Course();
        $db->course_name = $request->course_name;
        $photoName = time().'.'.$request->course_photo->getClientOriginalExtension();
        $request->course_photo->move(public_path('course'), $photoName);
        $db->course_photo ="https://www.icajobguarantee.com/android/public/course/". $photoName;
        $db->save();
        return redirect()->action('Admin\CourseController@index');
    }

